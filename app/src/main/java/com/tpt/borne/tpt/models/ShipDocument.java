public class ShipDocument {

    private String shipDocumentationid;
    private String file;
    private String fileName;


    public String getShipDocumentationid() {
        return shipDocumentationid;
    }

    public void setShipDocumentationid(String shipDocumentationid) {
        this.shipDocumentationid = shipDocumentationid;
    }

    public String getFile() {
        return file;
    }

    public void setFile(String file) {
        this.file = file;
    }

    public String getFileName() {
        return fileName;
    }

    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
